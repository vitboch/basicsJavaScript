//3 Operators
let currentYear = 2022
const birthYear = 1981

const age = currentYear - birthYear
console.log(age) //41

const a = 10
const b = 5

console.log(a + b) //15
console.log(a - b) //5
console.log(a * b) //50
console.log(a / b) //2
console.log(currentYear++) //2022
console.log(currentYear) //2023
console.log(--currentYear) //2022

let c = 32
c += a //c = c + a
c -= a //c = c - a
c *= a //c = c * a
c /= a //c = c / a
console.log(c) //32