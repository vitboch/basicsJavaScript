//3 Arrow functions
const arrow = (name) => {
    console.log('Hi -', name) //Hi - Lena
}
arrow('Vit') //Hi - Vit

const arrow2 = name => console.log('Hi -', name) //Hi - Ivan
arrow2('Ivan')

const pow2 = num => {
    return num ** 2
}
console.log(pow2(5)) //25

const pow3 = num => num ** 3
console.log(pow3(5)) //125



